const fs = require('fs');
const path = require('path');

const API_ROOT = process.env.OPEN5E_API_URL || 'https://api.open5e.com';
const LIMIT = Number(process.env.OPEN5E_API_LIMIT || 500);
const TARGET = path.resolve(__dirname, '../../monster-creator/scripts/open5e/creatures.generated.mjs');

const fetchJson = async (url) => {
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Open5E request failed: ${response.status} ${response.statusText}`);
  }
  return response.json();
};

const main = async () => {
  const monsters = [];
  let page = 1;
  let expectedCount = null;

  while (true) {
    const url = `${API_ROOT.replace(/\/+$/, '')}/v2/creatures/?limit=${LIMIT}&page=${page}`;
    process.stdout.write(`Fetching ${url}\n`);
    const payload = await fetchJson(url);
    if (expectedCount === null) expectedCount = Number(payload.count || 0);

    const results = Array.isArray(payload.results) ? payload.results : [];
    monsters.push(...results);

    if (!payload.next || !results.length) break;
    page += 1;
  }

  const generatedAt = new Date().toISOString();
  const body = [
    '// Generated by tools/open5e/generate-open5e-data.js. Do not edit by hand.',
    `export const OPEN5E_CREATURES = ${JSON.stringify(monsters)};`,
    '',
    'export const OPEN5E_DATA_META = {',
    `  generatedAt: ${JSON.stringify(generatedAt)},`,
    `  sourceUrl: ${JSON.stringify(`${API_ROOT.replace(/\/+$/, '')}/v2/creatures/`)},`,
    `  count: ${monsters.length},`,
    `  expectedCount: ${Number.isFinite(expectedCount) ? expectedCount : 'null'}`,
    '};',
    ''
  ].join('\n');

  fs.writeFileSync(TARGET, body, 'utf8');
  process.stdout.write(`Wrote ${monsters.length} creatures to ${TARGET}\n`);
};

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
